void show(void *u, int w, int h)
{
	int (*univ) = u;
	printf("\033[H");
	
	for(int i = 0; i < h; i++){
		for(int j = 0; j < w; j++)
			printf(univ[(i * h) + j] ? "\033[07m  \033[m" : "  ");

		printf("\033[E");
	}

	fflush(stdout);
}

void evolve(void *u, int w, int h)
{
	unsigned (*univ) = u;
	unsigned new[900];
	int sum_y = 0;

	for(int y = 0; y < h; y++) {
		for(int x = 0; x < w; x++)
		{
			int n = 0;
			for (int y1 = y -1; y1 <= y + 1; y1++)
			{
				int sum = 0;
				int mod_y = ((y1 < 0) ? y1 + h : ((y1 == h) ? 0 : y1));

				for(int i = 0; i < mod_y; i++)
					sum += h;

				for (int x1 = x -1; x1 <= x + 1; x1++)
			 	{
					int mod_x = ((x1 < 0) ? x1 + w : (x1 == w) ? 0 : x1);
					int sum_ = sum + mod_x;

					if (univ[sum_])
						n++;
				}
			}

			if (univ[sum_y + x]) n--;
			new[sum_y + x] = (n == 3 || (n == 2 && univ[sum_y + x]));
		}

		sum_y += h;
	}

	sum_y = 0;
	for(int y = 0; y < h; y++)
	{
		for(int x = 0; x < w; x++)
			univ[sum_y + x] = new[sum_y + x];

		sum_y += h;
	}
}

