void show(unsigned univ[])
{
	int w = 30, h = 30;

	printf("\033[H");
	
	for(int i = 0; i < h; i++){
		for(int j = 0; j < w; j++)
			printf(univ[(i * h) + j] ? "\033[07m  \033[m" : "  ");

		printf("\033[E");
	}

	fflush(stdout);
}

void evolve(unsigned univ[], unsigned new[])
{
	register int sum_y = 0;
	register int x, y;
	register int w = 30, h = 30;

	for(y = 0; y < h; y++) {
		for(x = 0; x < w; x++)
		{
			register int n = 0;
			register int y1; 
			for (y1 = y -1; y1 <= y + 1; y1++)
			{
				register int sum = 0;
				register int mod_y;
				register int i;

				if(y1 < 0)
					mod_y = y1 + h;
				else if(y1 == h)
					mod_y = 0;
				else
					mod_y = y1;

				for(i = 0; i < mod_y; i++)
					sum += h;

				for (i = x -1; i <= x + 1; i++)
			 	{
					register int mod_x;
					if(i < 0)
						mod_x = i + w;
					else if(i == w)
						mod_x = 0;
					else
						mod_x = i;

					register int sum_ = sum + mod_x;

					if (univ[sum_])
						n++;
				}
			}

			if (univ[sum_y + x]) 
				n--;
			
			new[sum_y + x] = (n == 3 || (n == 2 && univ[sum_y + x]));
		}

		sum_y += h;
	}

	sum_y = 0;
	for(y = 0; y < h; y++)
	{
		for(x = 0; x < w; x++)
			univ[sum_y + x] = new[sum_y + x];

		sum_y += h;
	}
}

